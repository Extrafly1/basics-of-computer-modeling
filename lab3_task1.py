def middle_square_method(seed, N):
    """
    Генерация последовательности псевдослучайных чисел методом серединных квадратов.

    :param seed: начальное число (например, 0.583)
    :param N: количество чисел в последовательности
    :return: список сгенерированных псевдослучайных чисел
    """
    result = []  # Список для хранения последовательности
    current = seed  # Текущее число, начинаем с R0

    for _ in range(N):
        squared = str(current ** 2).replace('.', '')  # Убираем точку для получения целого числа
        # Убедимся, что длина строки не меньше 8 символов
        if len(squared) < 8:
            squared = squared.zfill(8)  # Добавляем нули спереди, если строка меньше 8 символов
        
        # Берем четыре средние цифры
        next_number = float('0.' + squared[2:6])  # Четыре средние цифры превращаем в следующее число
        result.append(next_number)  # Добавляем число в результат
        current = next_number  # Обновляем текущее число для следующей итерации

    return result
# Пример вызова
R0 = 0.583
N = 6
sequence = middle_square_method(R0, N)
print(sequence)